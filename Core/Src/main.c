extern rt_thread_t key_thread;
extern rt_thread_t oled_thread;
extern rt_thread_t finger_thread;
extern rt_thread_t wifi_thread;

extern rt_sem_t uart2_sem;
extern rt_sem_t finger_sem;
extern rt_sem_t wifi_sem;

extern rt_event_t system_event;

int main() {

    OLED_Init();

    OLED_showInfo_Boot();

    //线程创建
    key_thread = rt_thread_create(
            "KeyThread",
            key_thread_entry,
            RT_NULL,
            512,
            2,
            500);

    oled_thread = rt_thread_create(
            "OledThread",
            oled_thread_entry,
            RT_NULL,
            4096,
            3,
            500);

    finger_thread = rt_thread_create(
            "FPThread",
            finger_thread_entry,
            RT_NULL,
            512,
            3,
            500);

    wifi_thread = rt_thread_create(
            "WifiThread",
            wifi_thread_entry,
            RT_NULL,
            1024,
            3,
            500);

    //其他创建
    uart2_sem = rt_sem_create(
            "UART2Sem",
            0,
            RT_IPC_FLAG_FIFO);

    finger_sem = rt_sem_create(
            "FingerSem",
            1,
            RT_IPC_FLAG_FIFO);

    wifi_sem = rt_sem_create(
            "WifiSem",
            0,
            RT_IPC_FLAG_FIFO);

    system_event = rt_event_create(
            "InputEvent",
            RT_IPC_FLAG_FIFO);

    //线程启动
    if (key_thread != RT_NULL) rt_thread_startup(key_thread);
    else rt_kprintf("key startup error");
    if (oled_thread != RT_NULL) rt_thread_startup(oled_thread);
    else rt_kprintf("oled startup error");
    if (finger_thread != RT_NULL) rt_thread_startup(finger_thread);
    else rt_kprintf("finger startup error");
    if (wifi_thread != RT_NULL) rt_thread_startup(wifi_thread);
    else rt_kprintf("wifi recv startup error");
}

